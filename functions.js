// Arrow functions (new with es6)

//when using {}, you need a return statement
const add = (n1, n2) => {
  return n1 + n2;
}

// if we don't {}, then its implicitly returned
const add1 = (n1, n2) => n1 + n2


//es5 declaring

function add3(n1,n2) {
  return n1 + n2
}

console.log(add(2,3))
console.log(add1(2,3))
console.log(add3(2,3))

